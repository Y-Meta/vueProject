    //参数拼接
    public static String getParamString(Map<String, Object> maps) {
        StringBuilder params = new StringBuilder();

        for (String key : maps.keySet()) {
            String str="";
            try {
                if (maps.get(key) instanceof String){
                    //字符串类型的  编码
                str = URLEncoder.encode((String) maps.get(key),"UTF-8");
                    params.append(key + "=").append(str).append("&");
                }else {
                    params.append(key+"=").append(maps.get(key)).append("&");
                }
            } catch (UnsupportedEncodingException e) {
                e.printStackTrace();
            }

        }
        String param = "";
        param = params.substring(0, params.length() - 1);
        return param;

    }

